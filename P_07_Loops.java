public class P_07_Loops {
    public static void main(String[] args) {
        for (int a = 0; a <= 10; a++) {
            System.out.println(a * 10);
        }
        int i = 0;
        while (i<5){
            System.out.println((i));
            i++;
        }

        do {
            System.out.println(i);
            i++;
        }while (i<7);
    }
}
